package com.wine.base.bean;

public class ImageSetting {
    private Double compressedRate;

    private Integer compressedPix;

    private Integer iconPix;

    public Double getCompressedRate() {
        return compressedRate;
    }

    public void setCompressedRate(Double compressedRate) {
        this.compressedRate = compressedRate;
    }

    public Integer getCompressedPix() {
        return compressedPix;
    }

    public void setCompressedPix(Integer compressedPix) {
        this.compressedPix = compressedPix;
    }

    public Integer getIconPix() {
        return iconPix;
    }

    public void setIconPix(Integer iconPix) {
        this.iconPix = iconPix;
    }
}